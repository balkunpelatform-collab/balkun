// این فایل را جایگزین کامل فایل فعلی با همین مسیر در پروژه کنید.
//
// تولید اسلاگ (آدرس اینترنتی) از روی عنوان یا اسلاگ دستی فارسی/عربی/انگلیسی پست بلاگ.
//
// 🆕 رفع باگ ریشه‌ای ۴۰۴ برای اسلاگ فارسی (نسخه‌ی کامل‌تر):
// نسخه‌ی قبلی این فایل فقط علائم نگارشی عربی (؛ ، ؟ ٪) را که باعث ۴۰۴ در URL
// می‌شدند حذف می‌کرد. اما یک علت خیلی رایج‌تر باقی مانده بود که باعث می‌شد
// خیلی از اسلاگ‌های فارسی معمولی (نه فقط آن‌هایی که علامت نگارشی داشتند) هم
// ۴۰۴ بدهند:
//
// حروف عربی هم‌شکل با فارسی: صفحه‌کلیدها و متن‌های کپی‌شده از منابع مختلف
// (واتساپ، اینستاگرام، ورد و...) گاهی به‌جای «ی» فارسی از «ي» عربی، و به‌جای
// «ک» فارسی از «ك» عربی استفاده می‌کنند. این حروف کاملاً هم‌شکل و از نظر چشم
// غیرقابل‌تشخیص‌اند، اما از نظر کد، دو کاراکتر کاملاً متفاوت محسوب می‌شوند.
// نتیجه: اسلاگ ذخیره‌شده در دیتابیس گاهی با اسلاگی که بعداً برای باز کردن
// همان صفحه ساخته می‌شود، از نظر بایت یکی نیست — و همین یعنی ۴۰۴.
// این نسخه، پیش از هر پردازش دیگری، هر دو حرف عربی را به معادل فارسی‌شان
// تبدیل می‌کند تا این تناقض هرگز پیش نیاید.
//
// در کنار آن، دو محافظ اضافه هم اضافه شد:
// ۱) `.normalize("NFC")`: بعضی صفحه‌کلیدها (خصوصاً موبایل) همان حروف را با
//    ترکیب یونیکد متفاوتی ذخیره می‌کنند که ظاهرش یکسان ولی بایت‌هایش فرق دارد.
// ۲) حذف صریح نویسه‌های نامرئی مثل نیم‌فاصله (ZWNJ) که در تایپ فارسی رایج‌اند
//    ولی هیچ معنایی در آدرس اینترنتی ندارند.
//
// رنج مجاز حروف/اعداد فارسی-عربی (بدون علائم نگارشی و اعراب) دست‌نخورده ماند،
// چون خودش قبلاً درست تعریف شده بود.
const PERSIAN_ARABIC_LETTERS_AND_DIGITS =
  "\\u0621-\\u064A\\u0660-\\u0669\\u066E-\\u06D3\\u06D5\\u06EE-\\u06FC\\u06FF";

export function generateSlugFromTitle(title: string): string {
  return title
    .normalize("NFC")
    // یکسان‌سازی حروف عربی هم‌شکل با معادل فارسی‌شان — مهم‌ترین علت باقی‌مانده‌ی ۴۰۴
    .replace(/\u064A/g, "\u06CC") // ي عربی -> ی فارسی
    .replace(/\u0643/g, "\u06A9") // ك عربی -> ک فارسی
    // حذف نویسه‌های نامرئی (نیم‌فاصله و مشابه) پیش از پردازش اصلی
    .replace(/[\u200B-\u200F\uFEFF]/g, "")
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(new RegExp(`[^${PERSIAN_ARABIC_LETTERS_AND_DIGITS}a-z0-9-]`, "g"), "")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "");
}