// تولید اسلاگ (آدرس اینترنتی) از روی عنوان فارسی/انگلیسی پست بلاگ.
// حروف فارسی/عربی و انگلیسی و اعداد نگه داشته می‌شوند، فاصله‌ها به خط تیره تبدیل
// و بقیه کاراکترهای خاص حذف می‌شوند.
//
// 🆕 رفع باگ ریشه‌ای ۴۰۴ (پیدا شد با تست پست واقعی کارفرما):
// رنج قبلی \u0600-\u06FF کل «بلوک یونیکد عربی» را مجاز می‌کرد، اما این بلوک فقط
// شامل حروف فارسی/عربی نیست — علائم نگارشی عربی مثل «؛» (U+061B)، «،» (U+060C)،
// «؟» (U+061F) و «٪» (U+066A) هم داخل همین بازه‌اند. یعنی اگر عنوان پست یک «؛»
// داشت (دقیقاً مثل تست کارفرما: «دهکده سوئیسی؛ گریزی...»)، آن «؛» عیناً وارد
// اسلاگ می‌شد. علامت‌های «؛»، «،»، «؟» و «٪» در آدرس اینترنتی (URL) کاراکترهای
// رزرو/خاص هستند (طبق استاندارد RFC 3986) و باعث می‌شوند مرورگر/سرور/CDN مسیر را
// درست تشخیص ندهند — دقیقاً همان ۴۰۴ که کارفرما دید. راه‌حل: به‌جای مجاز کردن کل
// بلوک، فقط بازه‌های واقعی «حروف» و «اعداد» فارسی/عربی مجازند؛ علائم نگارشی و
// اعراب (تشکیل) به‌طور کامل حذف می‌شوند. روی اسلاگ‌های قبلی (بدون علامت نگارشی)
// هیچ تغییری ایجاد نمی‌کند — فقط علامت‌های نگارشی که باعث ۴۰۴ می‌شدند حذف می‌شوند.
const PERSIAN_ARABIC_LETTERS_AND_DIGITS =
  "\\u0621-\\u064A\\u0660-\\u0669\\u066E-\\u06D3\\u06D5\\u06EE-\\u06FC\\u06FF";

export function generateSlugFromTitle(title: string): string {
  return title
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(new RegExp(`[^${PERSIAN_ARABIC_LETTERS_AND_DIGITS}a-z0-9-]`, "g"), "")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "");
}