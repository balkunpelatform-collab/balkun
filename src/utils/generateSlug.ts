// 🆕 تصمیم جدید: اسلاگ بلاگ دیگر نباید فارسی باشد.
// اسلاگ فارسی در عمل (روی هاست/CDN فعلی) باعث ۴۰۴ می‌شد، برخلاف چیزی که در تلاش
// قبلی برای رفع این باگ (یکسان‌سازی حروف عربی/فارسی هم‌شکل) پیش‌بینی شده بود. به
// همین دلیل الان به‌جای تلاش برای «درست کردن» اسلاگ فارسی، اسلاگ فارسی از همان ابتدا
// در فرم ادمین رد می‌شود و کاربر مجبور است اسلاگ را با حروف انگلیسی وارد کند.
//
// دو تابع جدید زیر برای همین منظور اضافه شدند و در فرم ادمین بلاگ و در روت‌های
// POST/PATCH بلاگ استفاده می‌شوند. تابع قدیمی generateSlugFromTitle دست‌نخورده
// نگه داشته شده (برای سازگاری با آینده)، اما دیگر در مسیر بلاگ استفاده نمی‌شود.

// آیا رشته‌ی داده‌شده یک اسلاگ معتبر انگلیسی است؟ (فقط حروف کوچک a-z، عدد، خط تیره؛
// بدون خط تیره‌ی ابتدا/انتها یا پشت‌سرهم)
export function isEnglishSlug(slug: string): boolean {
  return /^[a-z0-9]+(-[a-z0-9]+)*$/.test(slug);
}

// پاک‌سازی سطحی یک اسلاگ انگلیسی: فاصله/زیرخط -> خط‌تیره، حروف بزرگ -> کوچک،
// خط‌تیره‌های تکراری/ابتدا/انتها حذف می‌شوند. برخلاف generateSlugFromTitle، این تابع
// هیچ حرف غیرانگلیسی (از جمله فارسی/عربی) را حذف نمی‌کند تا حضورشان قابل تشخیص و رد
// شود — تشخیص معتبر بودن نهایی همیشه باید با isEnglishSlug انجام شود.
export function normalizeEnglishSlug(input: string): string {
  return input
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "");
}

// ==========================================
// نسخه‌ی قدیمی — دیگر در مسیر بلاگ استفاده نمی‌شود، فقط برای سازگاری نگه داشته شده.
// ==========================================
//
// تولید اسلاگ (آدرس اینترنتی) از روی عنوان یا اسلاگ دستی فارسی/عربی/انگلیسی.
const PERSIAN_ARABIC_LETTERS_AND_DIGITS =
  "\\u0621-\\u064A\\u0660-\\u0669\\u066E-\\u06D3\\u06D5\\u06EE-\\u06FC\\u06FF";

export function generateSlugFromTitle(title: string): string {
  return title
    .normalize("NFC")
    .replace(/\u064A/g, "\u06CC") // ي عربی -> ی فارسی
    .replace(/\u0643/g, "\u06A9") // ك عربی -> ک فارسی
    .replace(/[\u200B-\u200F\uFEFF]/g, "")
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(new RegExp(`[^${PERSIAN_ARABIC_LETTERS_AND_DIGITS}a-z0-9-]`, "g"), "")
    .replace(/-+/g, "-")
    .replace(/^-+|-+$/g, "");
}