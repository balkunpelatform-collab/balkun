// اعتبارسنجی حداقلی شکل JSON خوانده‌شده از دیتابیس برای کلید "terms" — همان منطق
// isValidAboutContent اما برای آرایه‌ی پویای بندها.
function isValidTermsContent(value: unknown): value is TermsPageContent {
  if (!value || typeof value !== "object") return false;
  const v = value as Record<string, unknown>;
  if (typeof v.heroTitle !== "string" || !v.heroTitle.trim()) return false;
  if (typeof v.heroDescription !== "string" || !v.heroDescription.trim()) return false;
  if (!Array.isArray(v.sections) || v.sections.length < TERMS_SECTIONS_MIN_COUNT) return false;
  return v.sections.every(
    (item) =>
      item &&
      typeof item === "object" &&
      typeof (item as TermsSectionItem).title === "string" &&
      (item as TermsSectionItem).title.trim() &&
      typeof (item as TermsSectionItem).body === "string" &&
      (item as TermsSectionItem).body.trim()
  );
}

// خواندن متن زنده‌ی صفحه‌ی قوانین و مقررات — در src/app/terms/page.tsx (نمایش عمومی
// سایت) و در GET /api/admin/site-content/terms (پیش‌نمایش فرم ویرایش ادمین) استفاده
// می‌شود.
export async function getTermsPageContent(): Promise<TermsPageContent> {
  try {
    const { data, error } = await supabaseAdmin
      .from("site_content")
      .select("content")
      .eq("key", "terms")
      .maybeSingle();

    if (error || !data || !isValidTermsContent(data.content)) {
      return DEFAULT_TERMS_CONTENT;
    }

    return data.content;
  } catch (err) {
    console.error("Error fetching terms page content, falling back to default:", err);
    return DEFAULT_TERMS_CONTENT;
  }
}

// ذخیره‌ی متن جدید صفحه‌ی قوانین و مقررات — فقط در PUT /api/admin/site-content/terms
// (منحصراً SUPER_ADMIN) استفاده می‌شود.
export async function saveTermsPageContent(
  content: TermsPageContent,
  adminId: string
): Promise<void> {
  const { error } = await supabaseAdmin.from("site_content").upsert(
    {
      key: "terms",
      content,
      updatedAt: new Date().toISOString(),
      updatedBy: adminId,
    },
    { onConflict: "key" }
  );

  if (error) {
    throw new Error(`خطا در ذخیره‌سازی متن صفحه‌ی قوانین و مقررات: ${error.message}`);
  }
}