// اعتبارسنجی حداقلی شکل JSON خوانده‌شده از دیتابیس، تا اگر به هر دلیلی محتوای
// ستون content ناقص یا خراب بود، به‌جای کرش کردن صفحه‌ی عمومی درباره‌ما، همان
// مقدار پیش‌فرض جایگزین شود.
function isValidAboutContent(value: unknown): value is AboutPageContent {
  if (!value || typeof value !== "object") return false;
  const v = value as Record<string, unknown>;
  if (typeof v.heroTitle !== "string" || !v.heroTitle.trim()) return false;
  if (typeof v.heroDescription !== "string" || !v.heroDescription.trim()) return false;
  if (typeof v.valuesSectionTitle !== "string" || !v.valuesSectionTitle.trim()) return false;
  if (!Array.isArray(v.values) || v.values.length !== ABOUT_PAGE_VALUES_COUNT) return false;
  return v.values.every(
    (item) =>
      item &&
      typeof item === "object" &&
      typeof (item as AboutPageValueItem).title === "string" &&
      (item as AboutPageValueItem).title.trim() &&
      typeof (item as AboutPageValueItem).description === "string" &&
      (item as AboutPageValueItem).description.trim()
  );
}

// خواندن متن زنده‌ی صفحه‌ی درباره‌ما — در src/app/about/page.tsx (نمایش عمومی سایت)
// و در GET /api/admin/site-content/about (پیش‌نمایش فرم ویرایش ادمین) استفاده می‌شود.
export async function getAboutPageContent(): Promise<AboutPageContent> {
  try {
    const { data, error } = await supabaseAdmin
      .from("site_content")
      .select("content")
      .eq("key", "about")
      .maybeSingle();

    if (error || !data || !isValidAboutContent(data.content)) {
      return DEFAULT_ABOUT_CONTENT;
    }

    return data.content;
  } catch (err) {
    console.error("Error fetching about page content, falling back to default:", err);
    return DEFAULT_ABOUT_CONTENT;
  }
}

// ذخیره‌ی متن جدید صفحه‌ی درباره‌ما — فقط در PUT /api/admin/site-content/about
// (منحصراً SUPER_ADMIN) استفاده می‌شود. upsert روی key="about" انجام می‌شود چون
// ممکن است این اولین باری باشد که کسی محتوای این کلید را ذخیره می‌کند.
export async function saveAboutPageContent(
  content: AboutPageContent,
  adminId: string
): Promise<void> {
  const { error } = await supabaseAdmin.from("site_content").upsert(
    {
      key: "about",
      content,
      updatedAt: new Date().toISOString(),
      updatedBy: adminId,
    },
    { onConflict: "key" }
  );

  if (error) {
    throw new Error(`خطا در ذخیره‌سازی متن صفحه‌ی درباره‌ما: ${error.message}`);
  }
}

